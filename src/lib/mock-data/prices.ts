import type { Price } from "@/lib/types";

// ─── Helper ──────────────────────────────────────────────────
let priceId = 0;

function p(
  gameId: string,
  storeId: string,
  currentPrice: number,
  originalPrice: number,
  dealScore: number,
  edition = "standard",
  isAvailable = true,
): Price {
  priceId += 1;
  const discount =
    originalPrice > 0
      ? Math.round(((originalPrice - currentPrice) / originalPrice) * 100)
      : 0;
  return {
    id: `pr${priceId}`,
    gameId,
    storeId,
    edition,
    currentPrice,
    originalPrice,
    discount,
    currency: "USD",
    region: "US",
    url: "#",
    dealScore,
    lastUpdated: "2024-12-01T12:00:00Z",
    isAvailable,
  };
}

